    private void mniTextFormatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mniTextFormatActionPerformed
        PnlEditor pnlEditor = (PnlEditor) tbpContent.getSelectedComponent();
        if(pnlEditor == null){
            return;
        }
        
        int option = getFontChooser().showDialog(this);  //Muestra cuadro de dialogo para editar fuentes
        if (option == JFontChooser.CANCEL_OPTION){ //Si presionar cancelar no hace nada
            return;
        }
        
        String text = pnlEditor.getTxtpEditor().getSelectedText(); //Regresa el texto seleccionado
        if (text == null){ //Si no selecciona nada, retorna
            return;
        }
        
        Font font = getFontChooser().getSelectedFont();
        int start = pnlEditor.getTxtpEditor().getSelectionStart(); //Regresa desde donde empieza la seleccion
        StyledDocument style = pnlEditor.getTxtpEditor().getStyledDocument(); //Regresa las fuentes del documento
        pnlEditor.getTxtpEditor().setStyledDocument(getStyledDocuemt(font, style, start, text.length()));
        // Se le aplica la fuente al         (fuente para añadir, estilo del documento a cambiar, seleccion_inicial, seleccion_final
        // archivo con el que se trabaja
    }//GEN-LAST:event_mniTextFormatActionPerformed

