    private void mniOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mniOpenActionPerformed
        JFileChooser fileChooser =  new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
        "txt Files", "txt");
        
        fileChooser.setFileFilter(filter); //Colocar el filtro txt
        
        int option = fileChooser.showOpenDialog(this);
        if(option == JFileChooser.APPROVE_OPTION){
            openFile(fileChooser);
            JOptionPane.showMessageDialog(null, "Open File: "+ fileChooser.getSelectedFile().getName());
        } 
    }//GEN-LAST:event_mniOpenActionPerformed

    private void openFile(JFileChooser fileChooser) {
        File file = fileChooser.getSelectedFile();    
        
        fileConsequence = new FileConsequence(file); //Creacion de los Flujos
        
        PnlEditor pnlEditor = new PnlEditor();
        tbpContent.addTab(file.getName(),pnlEditor);
        
        try {
            pnlEditor.getTxtpEditor().setText(fileConsequence.read(file));
        } catch (IOException ex) {
            Logger.getLogger(FrmEditor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void mniSaveAsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mniSaveAsActionPerformed
         JFileChooser filechooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
        "txt Files", "txt");
        int index = tbpContent.getSelectedIndex();
        
        filechooser.setFileFilter(filter); //Coloca el filtro solo txt
        
        int option = filechooser.showSaveDialog(this);
        
        if (option == JFileChooser.CANCEL_OPTION){
            return;
        }
        
        String title = filechooser.getSelectedFile().getName();
        
        
        File fileNew = filechooser.getSelectedFile();
        PnlEditor pnlEditor = (PnlEditor) tbpContent.getSelectedComponent();
        
        if( (pnlEditor == null) || (".txt".equals(title))){            // Si se desea guardar un archivo txt y no ha abierto un Editor, retornar
            return;
        }
            
        saveFile(fileNew, pnlEditor);
        
        tbpContent.setTitleAt(index,title);
        JOptionPane.showMessageDialog(null,"File saved successfully");
    }//GEN-LAST:event_mniSaveAsActionPerformed

    private void saveFile(File fileNew, PnlEditor pnlEditor) {
        fileConsequence = new FileConsequence(fileNew);
        String textSave = pnlEditor.getTxtpEditor().getText();
        try {
            fileConsequence.write(fileNew, textSave);
        } catch (IOException ex) {
            Logger.getLogger(FrmEditor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
